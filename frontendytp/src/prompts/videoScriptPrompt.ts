export interface VideoScriptPromptParams {
  mindMapContent: string;
}

export const generateVideoScriptPrompt = ({ mindMapContent }: VideoScriptPromptParams): string => {
  return `请根据以下思维导图内容，生成一个完整的视频脚本：

思维导图内容：
${mindMapContent}

请严格按照以下格式生成脚本内容，格式必须完全一致：

**主标题**
一个吸引人的主标题，概括视频核心内容

**备选标题1**
第一个备选标题，角度稍有不同

**备选标题2**
第二个备选标题，角度稍有不同

**视频简介**
简要描述视频内容，包括观看价值和目标受众，50-100字

**章节内容**
第1章：开场引入
详细的开场内容，吸引观众注意力，介绍主题背景

第2章：核心要点一
详细阐述第一个核心观点，包含实例、数据支撑

第3章：核心要点二
详细阐述第二个核心观点，提供解决方案或方法论

第4章：实践应用
具体的应用方法和实践建议

第5章：总结回顾
总结要点，给出行动建议，鼓励互动

重要格式要求：
1. 每个章节必须以"第X章："开头，后面紧跟章节标题
2. 章节标题后换行，然后是章节详细内容
3. 每章节内容200-400字，语言生动有趣
4. 不要在章节标题中使用方括号[]
5. 严格按照上述格式输出，不要添加额外的标记符号`;
};

